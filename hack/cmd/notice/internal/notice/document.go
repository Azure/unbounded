// Package notice contains the core types and orchestration for the NOTICE
// generator: the on-disk schema, the Collector interface, and the helpers
// that ecosystem packages use to assemble entries.
package notice

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

// Document is the YAML root of the NOTICE file.
type Document struct {
	Notices []Entry `yaml:"notices"`
}

// Entry describes one direct dependency.
type Entry struct {
	Dependency string    `yaml:"dependency"`
	Ecosystem  string    `yaml:"ecosystem"`
	Copyright  []string  `yaml:"copyright"`
	License    []License `yaml:"license"`
}

// License attributes one license to a dependency, with a stable link to its
// authoritative text.
type License struct {
	Name string `yaml:"name"`
	Link string `yaml:"link"`
}

// HeaderComment is the YAML comment block prepended to the rendered NOTICE.
const HeaderComment = `# Generated by hack/cmd/notice. DO NOT EDIT.
# Run 'make notice' to regenerate. Source: go.mod + frontend/package.json.
`

// Build runs precheck and collect on every collector and returns a Document
// with entries sorted alphabetically by Dependency.
func Build(root string, collectors []Collector) (*Document, error) {
	for _, c := range collectors {
		if err := c.Precheck(root); err != nil {
			return nil, fmt.Errorf("%s precheck: %w", c.Name(), err)
		}
	}

	var all []Entry

	for _, c := range collectors {
		entries, err := c.Collect(root)
		if err != nil {
			return nil, fmt.Errorf("%s collect: %w", c.Name(), err)
		}

		all = append(all, entries...)
	}

	sort.SliceStable(all, func(i, j int) bool {
		return all[i].Dependency < all[j].Dependency
	})

	return &Document{Notices: all}, nil
}

// Render serializes the Document as YAML preceded by HeaderComment.
func Render(doc *Document) ([]byte, error) {
	var buf bytes.Buffer

	enc := yaml.NewEncoder(&buf)
	enc.SetIndent(2)

	if err := enc.Encode(doc); err != nil {
		return nil, fmt.Errorf("marshalling YAML: %w", err)
	}

	if err := enc.Close(); err != nil {
		return nil, fmt.Errorf("closing YAML encoder: %w", err)
	}

	return append([]byte(HeaderComment), buf.Bytes()...), nil
}

// Diff returns a minimal line-by-line summary of the differences between two
// rendered NOTICE bodies. It is intentionally tiny - just enough to point at
// what changed without pulling in a diff library.
func Diff(got, want string) string {
	gotLines := strings.Split(got, "\n")
	wantLines := strings.Split(want, "\n")

	maxLines := len(gotLines)
	if len(wantLines) > maxLines {
		maxLines = len(wantLines)
	}

	const context = 200
	if maxLines > context {
		maxLines = context
	}

	var b strings.Builder

	for i := 0; i < maxLines; i++ {
		var g, w string
		if i < len(gotLines) {
			g = gotLines[i]
		}

		if i < len(wantLines) {
			w = wantLines[i]
		}

		if g == w {
			continue
		}

		fmt.Fprintf(&b, "@@ line %d\n-%s\n+%s\n", i+1, g, w)
	}

	return b.String()
}
